public class Calculadora {
    /**
     * Este método é ultilizado para somar dois numeros inteiros
     * @param numeroUm este numero e o primeiro parametro do metodo 
     * @param numeroDois este e o segundo parametro do metodo
     * @return int o resultado desse metodo e a soma dos dois numeros.
     */

     public int somar ( int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
     }
}
